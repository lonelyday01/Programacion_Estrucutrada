public class PARK_P2T1_Tablas_de_Multiplicar{
    public static void main(String[] args){
        for(int i=1; i<11; i++){
            for (int j=1; j<11; j++){
                System.out.println(i+"x"+j+"="+i*j);
            }
        }
    }
}
